void LCD_Writ_Bus(uint8_t da)
{	
  SPI_transmit(da);
} 
void LCD_WR_DATA8(uint8_t da)
{
  TFT_DC_HIGH;
	LCD_Writ_Bus(da);
}  
 void LCD_WR_DATA(int da)
{
  TFT_DC_HIGH;
	LCD_Writ_Bus(da>>8);
	LCD_Writ_Bus(da);
}	  
void LCD_WR_REG(uint8_t da)	 
{
  TFT_DC_LOW;
	LCD_Writ_Bus(da);
}
 void LCD_WR_REG_DATA(int reg,int da)
{
    LCD_WR_REG(reg);
	LCD_WR_DATA(da);
}
